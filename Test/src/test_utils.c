static char const * test_array_cmp(void)
{
	unsigned char const arr1[SPHINCS_BYTES] = {
		0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
		0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17,
		0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D, 0x1E, 0x1F
	};
	unsigned char const arr2[SPHINCS_BYTES] = {
		0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
		0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17,
		0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D, 0x1E, 0x1F
	};
	unsigned char const arr3[SPHINCS_BYTES] = {
		0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
		0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
		0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
		0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF
	};

	mu_assert("Test case for (arr1 == arr1) failed.", array_cmp(arr1, arr1, SPHINCS_BYTES));
	mu_assert("Test case for (arr2 == arr1) failed.", array_cmp(arr2, arr1, SPHINCS_BYTES));
	mu_assert("Test case for (arr1 == arr2) failed.", array_cmp(arr1, arr2, SPHINCS_BYTES));
	mu_assert("Test case for (arr1 != arr3) failed.", !array_cmp(arr1, arr3, SPHINCS_BYTES));
	mu_assert("Test case for (arr3 != arr1) failed.", !array_cmp(arr3, arr1, SPHINCS_BYTES));

	return ""; /* Success */
}

static char const * test_array_is_zero(void)
{
	unsigned int const arr1[SPHINCS_BYTES] = {
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
	};
	unsigned int const arr2[SPHINCS_BYTES] = {
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
		0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x00
	};

	mu_assert("Test case for fully zero array arr1 failed.", array_is_zero(arr1, SPHINCS_BYTES));
	mu_assert("Test case for semi-zero array arr2 failed.", array_is_zero(arr2, SPHINCS_BYTES/2));
	mu_assert("Test case for non-zero array arr2 failed.", !array_is_zero(arr2, SPHINCS_BYTES));

	return "";
}

void run_test_utils(void)
{
	printf("### TEST_UTILS ###\n\n");

	mu_run_test(test_array_cmp, "test_array_cmp()");
	mu_run_test(test_array_is_zero, "test_array_is_zero()");

	printf("\n");
}
